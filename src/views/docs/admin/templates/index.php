# Templates

Templates are really just mid-level files which are usually where the page-specific markup lives. They are considered mid-level because they "guts" in-between the layout and any partials.

The templates listed on the index have already been registered in Devise. So, any new templates will need to be registered before they can be used or applied to other parts of Devise (e.g. Pages).

To register a new template, begin by clicking the "Register New Template" button in the top-righthand corner.